    public EstudianteView(User user) {
        super("Panel del Estudiante");
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        setMinimumSize(new Dimension(1100, 700));
        setLocationRelativeTo(null);

        // Root
        JPanel root = new JPanel(new BorderLayout());
        root.setBackground(UIConstants.BG_APP);
        setContentPane(root);

        // Encabezado
        HeaderPanel header = new HeaderPanel();
        root.add(header, BorderLayout.NORTH);

        // Cuerpo con margen
        JPanel body = new JPanel(new BorderLayout());
        body.setBorder(new EmptyBorder(18, 18, 18, 18));
        body.setBackground(UIConstants.BG_APP);
        root.add(body, BorderLayout.CENTER);

        // Lateral izquierdo (men√∫)
        SidebarPanel sidebar = new SidebarPanel();
        body.add(sidebar, BorderLayout.WEST);

        // Contenido principal (tarjeta perfil del estudiante)
        ProfileCardPanel card = new ProfileCardPanel(
                user.getNombres() + " " + user.getApellidos(),
                user.getPrograma().toString(),
                user.getId().toString(),
                user.getEmail(),
                user.getRol().toString(),
                user.getEmail()
        );
        body.add(card, BorderLayout.CENTER);
    }

